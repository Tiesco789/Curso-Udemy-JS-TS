const numero = 10;

if (numero >= 0 && numero <= 5) {
  console.log('o numero está entre zero e cinco.');
} else if (numero >= 6 && numero <= 8) {
	console.log('o numero está entre seis e oito');
} else if (numero >= 9 && numero <= 11) {
	console.log('o numero está entre nove e onze');
} else {
	console.log('o numero não esta entre zero e onze');
}
